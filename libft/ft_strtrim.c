#include "libft.h"

int	is_in_set(char c, const char *set)
{
	while (*set)
	{
		if (c == *set)
			return (1);
		set++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	int		start;
	int		end;
	char	*trimed;
	int		i;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && is_in_set(s1[start], set))
		start++;
	while (end > start && is_in_set(s1[end - 1], set))
		end--;
	trimed = (char *)malloc(sizeof(char) * (end - start + 1));
	if (!trimed)
		return (NULL);
	i = 0;
	while (start < end)
		trimed[i++] = s1[start++];
	trimed[i] = '\0';
	return (trimed);
}
